package Array;

public class Array {
    public static void main(String[] args) {
        int arr[] = new int[10];
        arr[0] = 5;
        arr[5] = 17;
        System.out.println(arr[0]);
        System.out.println(arr[5]);

        char[] cArray = new char[10];
        double[] dArray = new double[10];
        cArray[1] = 'a';
        dArray[1] = 7.5;
        System.out.println(cArray[1]);
        System.out.println(dArray[1]);
    }
}
